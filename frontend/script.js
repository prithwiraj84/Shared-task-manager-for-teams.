let account;

async function connectWallet() {
    if (window.ethereum) {
        try {
            const accounts = await ethereum.request({ method: 'eth_requestAccounts' });
            account = accounts[0];
            document.getElementById("connectWallet").innerText = "Connected";
        } catch (error) {
            console.error("Wallet connection failed", error);
        }
    } else {
        alert("Please install MetaMask!");
    }
}

document.getElementById("connectWallet").addEventListener("click", connectWallet);

const web3 = new Web3(Web3.givenProvider || "http://localhost:8545");
const contract = new web3.eth.Contract(abi, contractAddress);

async function createNFT() {
    const metadata = document.getElementById('metadata').value;
    const price = web3.utils.toWei(document.getElementById('price').value, 'ether');

    try {
        await contract.methods.createNFT(metadata, price).send({ from: account });
        alert("NFT Created Successfully!");
    } catch (error) {
        console.error("Minting failed", error);
        alert("Minting Failed");
    }
}

async function listNFT() {
    const nftId = document.getElementById('listId').value;
    const price = web3.utils.toWei(document.getElementById('listPrice').value, 'ether');

    try {
        await contract.methods.listNFT(nftId, price).send({ from: account });
        alert("NFT Listed Successfully!");
    } catch (error) {
        console.error("Listing failed", error);
        alert("Listing Failed");
    }
}

async function buyNFT() {
    const nftId = document.getElementById('buyId').value;

    try {
        const price = await contract.methods.nfts(nftId).call();
        await contract.methods.buyNFT(nftId).send({ from: account, value: price.price });

        alert("NFT Purchased Successfully!");
    } catch (error) {
        console.error("Purchase failed", error);
        alert("Purchase Failed");
    }
}
